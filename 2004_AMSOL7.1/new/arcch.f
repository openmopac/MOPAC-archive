      SUBROUTINE ARCCH(NDIVS,NARCVL,IPSUM)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER FOUND
      DIMENSION DOTS(150)
      DATA DOTS/2,6,14,22,36,48,66,88,110,134,164,190,226,258,296,
     &  336,380,424,472,522,576,633,692,747,816,879,948,1017,1094,
     &  1169,1246,1327,1412,1499,1586,1673,1772,1869,1970,
     &  2069,2174,2279,2390,2501,2610,
     &  2731,2852,2967,3098,3223,3354,3485,3622,3751,3898,4037,
     &  4176,4327,4478,
     &  4629,4788,4937,5014,5263,5432,5591,5764,5941,6114,6289,
     &  6470,6655,6838,
     &  7025,7214,7401,7610,7803,8002,8209,8412,8617,8828,9041,
     &  9254,9481,9706,
     &  9923,10164,10385,10616,10849,11088,11323,11564,
     &  11811,12056,12305,12562,12811,13068,13329,13590,13849,14118,
     &  14389,14662,
     &  14939,15210,15489,15774,16057,16344,16627,16928,17217,17520,
     &  17817,18120,18425,18736,19041,19356,19675,19986,20307,
