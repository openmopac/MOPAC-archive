     &  24004,24343,
     &  24704,25059,25416,25779,26146,26501,26876,27257,27624,27997,
     &  28386,28761/
      I=1
      FOUND=0
53    IF (DOTS(I).LT.NDIVS) THEN
         I=I+1
      ELSE
         IF (DOTS(I).EQ.NDIVS) THEN
         NARCVL=I
         IF (I.NE.1) THEN
         IPSUM=DOTS(I)
         ELSE
         IPSUM=DOTS(1)
         ENDIF
         ELSE
         NARCVL=(I-1)
         IF (I.NE.1) THEN
         IPSUM=DOTS(NARCVL)
         ELSE
         IPSUM=DOTS(1)
         ENDIF
         ENDIF
         FOUND=1
      ENDIF
      IF (FOUND.EQ.0) GOTO 53
      RETURN
      END
